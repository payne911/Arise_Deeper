package com.payne.games.map.generator.algos.drunkard;


public class Drunkard {
    private int x, y;


    public Drunkard(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
